package Negocio;
/* Atributos dos Garçons, id, nome e preço
 * inicia a classe bebida
 * */
public class Garcon {
	private int id;
	private String nome;
	
	/*
	 * gets and sets
	 * */
	
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getNome() {
		return nome;
	}
	public void setNome(String nome) {
		this.nome = nome;
	}
	

}
